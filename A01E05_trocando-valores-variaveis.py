#!/usr/bin/python3

a = 5
b = 10

print ("Valor inicial de A: %d" % a)
print ("Valor inicial de B : %d" % b)

print ("TROCANDO OS VALORES DE A E B")

# Variável temporária para armazenar o valor atual de a ou b
temp = a

# Agora, realizamos a troca
a = b
b = temp

print ("Valor final de A: %d" % a)
print ("Valor final de B : %d" % b)


